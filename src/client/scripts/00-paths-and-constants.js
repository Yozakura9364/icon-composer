function appPath(rel) {
  const m = document.querySelector('meta[name="icon-composer-base"]');
  const raw = m && m.getAttribute('content') ? String(m.getAttribute('content')).trim() : '';
  const base = raw.replace(/\/$/, '');
  const p = rel.startsWith('/') ? rel : '/' + rel;
  return base ? base + p : p;
}

function applyHeaderIconPaths() {
  const downloadIcon = document.getElementById('downloadIcon');
  if (downloadIcon) downloadIcon.src = appPath('/assets/icons/download.svg');
  const settingsIcon = document.getElementById('settingsIcon');
  if (settingsIcon) settingsIcon.src = appPath('/assets/icons/setting.svg');
  const textLayerLoadLocalFontsIcon = document.getElementById('textLayerLoadLocalFontsIcon');
  if (textLayerLoadLocalFontsIcon) textLayerLoadLocalFontsIcon.src = appPath('/assets/icons/font.svg');
  const resetInfoTextContentIcon = document.getElementById('resetInfoTextContentIcon');
  if (resetInfoTextContentIcon) resetInfoTextContentIcon.src = appPath('/assets/icons/reset.svg');
  updateToggleAllInfoLayerVisibilityButton();
}

